void loadPosition()
{
    int k=0;
    for(int i=0;i<8;i++)
    for(int j=0;j<8;j++)
     {
       int n = board[i][j];
       if (!n) continue;
       int x = abs(n)-1;
       int y = n>0?1:0;
       
       		f[k].setTextureRect( IntRect(size*x,size*y,size,size) );
      		f[k].setPosition(size*j,size*i);
      	 	k++;
	   
     }
    for(int i=0;i<position.length();i+=5)
      move(position.substr(i,4));
}



